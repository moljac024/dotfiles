#!/usr/bin/env bash

################################################################################
### Baseline
################################################################################

remove () {
  if [[ -e $1 ]]; then
    rm $1
  fi

  if [[ -d $1 ]]; then
    rm -rf $1
  fi

  if [[ -e "$1.old"]]; then
    mv "$1.old" $1
  fi
}


################################################################################


################################################################################
### Bash
################################################################################

remove $HOME/.bash_profile
remove $HOME/.bashrc

remove $HOME/.bash_it

################################################################################


################################################################################


################################################################################
### Fish
################################################################################

remove $HOME/.config/fish

################################################################################


################################################################################
### Git
################################################################################

remove $HOME/.gitconfig
remove $HOME/.gitignore_global

################################################################################


################################################################################
### Vim
################################################################################

remove $HOME/.vim

################################################################################


################################################################################
### Ripgrep
################################################################################

remove $HOME/.ripgreprc

################################################################################


################################################################################
### Tmux
################################################################################

remove $HOME/.tmux.conf

################################################################################


################################################################################
### Tmuxinator bash completions
################################################################################

remove $HOME/.bash_completions/tmuxinator

################################################################################


################################################################################
### Asdf version manager ################################################################################

remove $HOME/.asdf

