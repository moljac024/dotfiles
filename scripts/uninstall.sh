#!/usr/bin/env bash

################################################################################
### Baseline
################################################################################

remove () {
  if [[ -f $1 ]]; then
    rm $1
  fi

  if [[ -d $1 ]]; then
    rm -rf $1
  fi

  # rm $@ > /dev/null 2> /dev/null
}


################################################################################


################################################################################
### Bash
################################################################################

remove $HOME/.bash_profile
remove $HOME/.bashrc

remove $HOME/.bash_it

