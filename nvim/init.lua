-- ############################################################################
-- Setup
-- ############################################################################

-- Enable faster module loading
vim.loader.enable()

-- Expose lib as a global variable
Lib = require("lib")

-- Monkey patch vim's keybind.set
Lib.command.patch_keymap_set()

-- ############################################################################
-- Options
-- ############################################################################

require("config.options")

-- ############################################################################
-- Plugins
-- ############################################################################

require("plugins")

-- ############################################################################
-- Keybindings
-- ############################################################################

-- We require keybinds last so that we can reference plugins and to make sure
-- plugins don't override any of our custom keybinds.

require("config.keybinds")

-- ############################################################################
-- Set up all the commands
-- ############################################################################

require("config.commands")
